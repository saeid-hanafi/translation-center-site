  <h1>درباره ارمنستان</h1>
  <p>ارمنستان از کشورهای بسیار زیبا در منطقه قفقاز جنوبی است. این کشور در حدفاصل دریای سیاه و دریای خزر قرار گرفته و مسیر ارتباطی ایران با اروپا است.
    نام رسمی آن جمهوری ارمنستان است اما در زبان محلی به آن “هایک” می گویند. زبان رسمی اهالی این کشور ارمنی است و نژاد آن ها  حاصل ادغام اقوام گوناگونی است که از در فلات ارمنستان می زیستند و مهاجرینی که از هند و اروپا به این سرزمین سفر کرده اند.
    پایتخت این کشور و مرکز مهم تجاری، سیاسی، فرهنگی و اقتصادی آن شهر ایروان است. گیومری، وانادزور و اچمیادزین از شهرهای مهم این کشور هستند.
    اقتصاد ارمنستان بر پایه ساخت و ساز و خدمات است همچنین به دلیل وجود معادن مس، آهن، طلا، نمک، بازالت و مرمر در زمینه تولیدات معدنی و صادرات آن نیز موفق بوده است. اما در سالهای اخیر توسعه گردشگری در این کشور به یکی از مهمترین منابع درآمدزایی در ارمنستان تبدیل شده است.  ورود گردشگران مختلف که عمدتا از روسیه، ایران، اتحادیه اروپا و گرجستان هستند نقش قابل توجهی در بهبود اقتصاد این کشور داشته است. توسعه زیرساخت های گردشگری، تنوع در محصولات گردشگری و برخورداری از توان فرهنگی و طبیعی بالا، معرفی جذابیت های این کشور به عنوان مقصد گردشگری در رسانه های بین المللی، لغو روادید برای کشورهای عضو اتحادیه اروپا و اتخاذ سیاست آسمان باز موجب شده که ارمنستان در بخش توسعه اقتصادی گردشگری رشد قابل توجهی داشته باشد.</p>
