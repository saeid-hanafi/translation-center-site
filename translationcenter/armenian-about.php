      رودخانه های ارمنستان عمدتا به ارس می پیوندند: هرازدان، آزات، ودی، وروتان، وخجی از رودهایی است که سرانجام بخشی از ارس می شود و رودهای دیگری که به دریاچه سوان می پیوندند.</p>
