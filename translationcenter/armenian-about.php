  <h3>آب و هوا</h3>
  <p>ارمنستان آب و هوای نسبتا متعادلی دارد. تابستان های این کشور از بادهای گرمی که از فلات ایران از جنوب وارد این کشور می شود، تاثیر می گیرد و زمستان های آن با افزایش فشار جو بسیار سرد می شود. دمای هوا در این کشور به شدت تابع فصول است و همین نکته تکلیف هر گردشگری را برای انتخاب فصل سفر به ارمنستان روشن می کند.
      قرار داشتن این سرزمین در ارتفاع ۴۰۰ تا ۴۰۰۰ متری از سطح دریا موجب تنوع اقلیمی گسترده ای در آن شده است؛ به طوری که اقلیم ارمنستان شامل مناطق نیمه بیابانی، استپی، جنگلی، آلپی و کوهستانی است.
